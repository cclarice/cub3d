#include "../cube3d.h"

int	ft_atoi(const char *str)
{
	long	r;
	int		n;

	n = 1;
	r = 0;
	while (*str == ' ' || (*str <= 13 && *str >= 9))
		++str;
	if (*str == '-')
		n = -1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str >= '0' && *str <= '9' && r <= 2147483648)
	{
		r = r * 10;
		r += *str - 48;
		++str;
	}
	if (r >= 2147483648 && n == 1)
		return (-1);
	if (r > 2147483648 && n == -1)
		return (0);
	return (n * r);
}
